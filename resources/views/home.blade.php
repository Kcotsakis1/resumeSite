    <p class="card-text">Google scripting quickly became one of my favorite tools. I created multiple scripts for office use. 
      <ul>
        <li><strong> 6 Hourly Observation recording sheet</strong> - This script will pull observations every 6 hours for select sites across the area for use in the <a href="https://forecast.weather.gov/product.php?site=CYS&issuedby=CYS&product=RTP&format=CI&version=1&glossary=1">Regional Temperatures and Precipitation Table</a>. Previously all observations were taken by hand, so this sheet automated the process, saving the forecasters time and reducing errors. All observations are archived each day.</li>
        <li><strong>River Flooding Notifications</strong> - This script uses the River Forecast Center's XML feed to find rivers that are forecasted to reach flood stage and notifies the forecasters of the flooding concern.</li>
        <li><strong>Spotter Training Signups </strong>- Storm Ready Spotter Trainings are conducted every spring to educate the public what to do in the event of a national weather disaster. This script allows us to use a Google Form to document the name, location (calculate lat/long from address), and email addresses of attendees, which is then added to the spotter database.</li></p>
